package hu.alkfejl.model;

public class Latnivalo {
    private String nev;
    private int ar;
    private int mettol;
    private int meddig;
    private String leiras;
    private int nepszeruseg;

    public String getNev() {
        return nev;
    }

    public void setNev(String nev) {
        this.nev = nev;
    }

    public int getAr() {
        return ar;
    }

    public void setAr(int ar) {
        this.ar = ar;
    }

    public int getMettol() {
        return mettol;
    }

    public void setMettol(int mettol) {
        this.mettol = mettol;
    }

    public int getMeddig() {
        return meddig;
    }

    public void setMeddig(int meddig) {
        this.meddig = meddig;
    }

    public String getLeiras() {
        return leiras;
    }

    public void setLeiras(String leiras) {
        this.leiras = leiras;
    }

    public int getNepszeruseg() {
        return nepszeruseg;
    }

    public void setNepszeruseg(int nepszeruseg) {
        this.nepszeruseg = nepszeruseg;
    }
}
